package app;

public class App {
    public static void main(String[] args) throws Exception {

        for (int i = 0; i <= 10; i = i +1){
            System.out.println("The count is " + i);
            System.out.println(" Lets keep counting. ");
        }
        for (int a = 10; a <= 20; a = a +1){
            System.out.println("The a count is " + a);
            System.out.println(" Lets keep counting. ");
        }
        for (int b = 10; b <= 100; b = b +10){
            System.out.println("The b count is " + b);
            System.out.println(" Lets keep counting. ");
        }
        for (int c = 1000; c <= 256000; c = c +1000){
            System.out.println("The c count is " + c);
            System.out.println(" Lets keep counting. ");
        }
        for (int d = 11; d <= 12; d = d +1){
            System.out.println("The d count is " + d);
            System.out.println(" Lets keep counting. ");
        }
        for (int e = 10; e >= 0; e = e -1){
            System.out.println("The e count is " + e);
            System.out.println(" Lets keep counting. ");
        }
      
      
    }
}
